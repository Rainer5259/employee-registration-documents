export const emailRegex = /^[\w-]+(\.[\w-]+)*@[\w-]+(\.[\w-]+)+$/

export const dateRegex =
  /^(0?[1-9]|[12][0-9]|3[01])\s+(jan|feb|mar|apr|may|jun|jul|aug|sep|oct|nov|dec)\s+\d{4}$/

export const phoneNumberRegex = /^\(\d{2}\) 9 \d{4}-\d{4}$/

export const nameRegex = /^[A-Za-z]+(([',. -][A-Za-z ])?[A-Za-z]*)*$/
